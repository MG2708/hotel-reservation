    private void mySQLConnection(int adult_num,int child_num,Date in_date, Date out_date,int rooms_num) throws SQLException{
        try {
            // Connect to the MySQL database using the JDBC driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            String url = "jdbc:mysql://localhost:3306/sit"; 
            String user = "root";
            String password = "toor";
            Connection con = DriverManager.getConnection(url, user, password);
            String s = "UPDATE radission_java SET adult_num = ?, child_num = ?, in_date = ?, out_date = ?, rooms_num = ? WHERE phone = ?";
            PreparedStatement ps = con.prepareStatement(s);
            ps.setInt(1, adult_num);
            ps.setInt(2, child_num);
            ps.setDate(3, new java.sql.Date(in_date.getTime()));
            ps.setDate(4, new java.sql.Date(out_date.getTime()));
            ps.setInt(5, rooms_num);
            ps.setString(6, number);


            int result = ps.executeUpdate(); //returns 1 if successful
            if (result == 1)
                System.out.println("Successfully Entered Record");
            else
                System.out.println("Error in inserting Record!"); 
        } catch (ClassNotFoundException | SQLException ex) {
            // Handle any exceptions that may occur while connecting to the database
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            System.exit(1);
        }
    }

