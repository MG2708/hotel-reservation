    public static boolean isTwoMonthsAfter(Calendar date1, Calendar date2) {
        Calendar date2TwoMonthsAfter = (Calendar) date1.clone();
        date2TwoMonthsAfter.add(Calendar.MONTH, 2);
        return date2.equals(date2TwoMonthsAfter);
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jDateChooser1.getDate() == null || jDateChooser2.getDate() == null){
           String message = "Check-in and check-out dates cannot be empty!";
            JOptionPane.showMessageDialog(new JFrame(), message, "Dialog", JOptionPane.ERROR_MESSAGE);
            return;
        } 
        
        Date checkIn = jDateChooser1.getDate();
        Date checkOut = jDateChooser2.getDate();
        
        Calendar cal1 = Calendar.getInstance();
        cal1.setTime(checkIn);

        Calendar cal2 = Calendar.getInstance();
        cal2.setTime(checkOut);
        
        Calendar calToday = Calendar.getInstance();
        Calendar calMaxDate = Calendar.getInstance();
        calMaxDate.add(Calendar.MONTH, 3);

        // Add 2 months to the check-in date
        cal1.add(Calendar.MONTH, 2);
        System.out.println(checkIn);
        System.out.println(checkOut);
        
        if (getAdults() + getChildren() > 4 && getRooms() == 1 ) {
            String message = "Sorry, you cannot select 1 room for more than 4 guests!";
            JOptionPane.showMessageDialog(new JFrame(), message, "Error",
        JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (checkIn.before(calToday.getTime())) {
            String message = "Check-in date cannot be before today!";
            JOptionPane.showMessageDialog(new JFrame(), message, "Error",
        JOptionPane.ERROR_MESSAGE);
            return;
        } else if (checkIn.after(calMaxDate.getTime())) {
            String message = "Check-in date cannot be more than 3 months from today!";
            JOptionPane.showMessageDialog(new JFrame(), message, "Error",
        JOptionPane.ERROR_MESSAGE);
            return;

        }else if (checkIn.after(checkOut)){
            String message = "Check-out cannot be before Check-in!";
            JOptionPane.showMessageDialog(new JFrame(), message, "Error",
        JOptionPane.ERROR_MESSAGE);
            return;
        }else if (!cal1.before(cal2)) {
            dispose();
            GetRoom userRoomChoice = new GetRoom(number, getRooms());
            userRoomChoice.setVisible(true);
        } else{
            String message = "Check-out cannot be more than 2 months after Check-in!";
            JOptionPane.showMessageDialog(new JFrame(), message, "Error",
        JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
                mySQLConnection(getAdults(),getChildren(),checkIn,checkOut,getRooms());
        } catch (SQLException ex) {
                // Handle any exceptions that may occur while connecting to the database
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                System.exit(1);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

